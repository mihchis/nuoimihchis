function toggleLanguageMenu() {
    const menu = document.getElementById('langDropdown');
    menu.classList.toggle('show');
}

// Close the dropdown if the user clicks outside of it
window.onclick = function (event) {
    if (!event.target.matches('.lang-toggle') && !event.target.matches('.lang-toggle *')) {
        var dropdowns = document.getElementsByClassName("lang-dropdown");
        for (var i = 0; i < dropdowns.length; i++) {
            var openDropdown = dropdowns[i];
            if (openDropdown.classList.contains('show')) {
                openDropdown.classList.remove('show');
            }
        }
    }
}

function setLanguage(lang) {
    // Update active button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });

    // Update text content
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.dataset.i18n;
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key]; // Use innerHTML to parse <strong>, <br>, etc.
        }
    });

    // Close menu
    document.getElementById('langDropdown').classList.remove('show');

    // Save preference (optional)
    localStorage.setItem('preferredLang', lang);
}

// Load saved language or default to Vietnamese
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLang') || 'vi';
    setLanguage(savedLang);
});
